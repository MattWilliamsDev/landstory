				<p class="storyline"> Prior to 1978, the largest coal extraction dragline
				in the world, the Marion Dragline, operated at Interlake. Flash forward just
				5 years and abandoned Interlake had already become an established local hotspot
				for ORV users, hunters, fisherman, horseback riders, and trappers... just
				to name a few. Finally, in 1999, the Indiana Department of Natural resources
				purchased the +3500 acres from the Interlake Foundation. Flash forward again
				just 9 years and IDNR hires the Landstory team to undertake the master planning
				and design of this 'wild west-esque' terrain. Setting out on a 3 day, down
				and dirty survey of the land on quads and other ORVs, the Landstory team
				was able to experience the site from a unique perspective. After careful inspection and environmental
				study of sweeping grassland, native forest and manmade lakes, the planning
				team developed a comprehensive master plan to provide IDNR with a plan for Interlake's future growth, as a vital recreational destination. </p>
				
				
				
